const context = (req, res, next) => {
  req.context = {};
  
  let token = Token.decode(req.body.game_key);
  let Model = token.multiplayer ? sails.models.gamemultiplayer : sails.models.game;

  Model
  .findOneById(token.game)
  .then(game => {
    token.game = game;
    req.context.token = token;
    next();
  })
  .catch(next);
};

